package com.example.meusdadosapp;

public class Pessoa {

    public String nome;
    public String sobrenome;
    public int idade;
    public Float salario;
    public String sexo;

    public Pessoa(String nome, String sobrenome, int idade, Float salario, String sexo){

        this.nome = nome;
        this.sobrenome = sobrenome;
        this.idade = idade;
        this.salario = salario;
        this.sexo = sexo;

    }
}
